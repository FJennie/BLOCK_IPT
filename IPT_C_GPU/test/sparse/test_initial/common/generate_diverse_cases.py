def write_case(c):
    case_dir = ROOT / c["id"]
    case_dir.mkdir(parents=True, exist_ok=True)
    atoms = "|".join(c["atoms"])
    lines = [
        f"export IPT_MATRIX_ID={c['id']}",
        f"export IPT_MOLECULE_BASIS={c['basis']}",
        f"export IPT_MOLECULE_CHARGE={c['charge']}",
        f"export IPT_MOLECULE_SPIN={c['spin']}",
    ]
    if c["active_e"] or c["active_o"]:
        lines.append(f"export IPT_ACTIVE_ELECTRONS={c['active_e']}")
        lines.append(f"export IPT_ACTIVE_ORBITALS={c['active_o']}")
    lines.append(f"export IPT_MOLECULE_ATOMS='{atoms}'")
    (case_dir / "case.env").write_text("\n".join(lines) + "\n")


def main():
    for c in DIVERSE_CASES:
        write_case(c)

    order = []
    for c in DIVERSE_CASES:
        if c["id"] not in order:
            order.append(c["id"])
    for item in ORIGINAL_ORDER:
        if item not in order:
            order.append(item)
    (ROOT / "common" / "diverse_matrix_order.txt").write_text(
        "\n".join(order[:100]) + "\n"
    )
    print(f"wrote {len(DIVERSE_CASES)} diverse cases")
    print(f"selected {min(100, len(order))} matrices")


if __name__ == "__main__":
    main()
